# ---------------------------------------------------------------------------
# 1. Confidence always in [0.0, 1.0]
# ---------------------------------------------------------------------------

class TestConfidenceRange:
    """Property: any confidence score produced by the rules engine must be
    clamped to [0.0, 1.0].  We test this by directly inspecting
    RuleEvaluationResult objects — the engine returns one per rule evaluated."""

    @given(raw_score=st.floats(min_value=-10.0, max_value=10.0, allow_nan=False))
    @settings(max_examples=200)
    def test_evaluation_result_confidence_in_range(self, raw_score: float):
        """Simulate the score that might be generated by any evaluator.
        After clamping it must stay within [0, 1]."""
        clamped = max(0.0, min(1.0, raw_score))
        result = RuleEvaluationResult(
            rule_id="rule-fuzz",
            rule_name="fuzz",
            passed=clamped >= 0.5,
            confidence=clamped,
            result="pass" if clamped >= 0.5 else "fail",
            details={},
        )
        assert 0.0 <= result.confidence <= 1.0, (
            f"confidence {result.confidence!r} out of [0,1] for raw_score={raw_score!r}"
        )


