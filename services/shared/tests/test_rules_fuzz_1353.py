# 2. Empty KDE → EvaluationSummary with no compliant verdict (None / not True)
# ---------------------------------------------------------------------------

class TestEmptyKdeNoCompliantVerdict:
    """Property: when the rules engine is given a canonical event with an
    empty KDE map, it must NOT return compliant=True (issue #1347).

    We mock the DB session so the engine loads zero rules — which is the
    empty-KDE analogue for our purposes.
    """

    @given(event_id=_nonempty_text_st, tlc=_nonempty_text_st)
    @settings(max_examples=100)
    def test_empty_kdes_no_compliant_verdict(self, event_id: str, tlc: str):
        from services.shared.rules.engine import RulesEngine

        mock_session = MagicMock()
        # Simulate an empty rule set (no rows returned by the DB)
        mock_session.execute.return_value.mappings.return_value.all.return_value = []

        engine = RulesEngine(db_session=mock_session)

        canonical_event = {
            "event_id": event_id,
            "event_type": "receiving",
            "tlc": tlc,
            "kdes": {},
            "product_description": "test product",
            "quantity": 1.0,
            "unit_of_measure": "EA",
            "location_gln": None,
            "location_name": None,
            "timestamp": "2026-04-20T00:00:00Z",
        }

        tenant_id = "00000000-0000-0000-0000-000000000001"

        with patch("services.shared.rules.engine.fetch_related_events", return_value=[]):
            summary = engine.evaluate_event(canonical_event, tenant_id=tenant_id)

        # Empty rule set → compliant must NOT be True (#1347)
        assert summary.compliant is not True, (
            f"Expected compliant to be None or False for empty rule set, got {summary.compliant!r}"
        )


