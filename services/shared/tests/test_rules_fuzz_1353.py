"""Property-based / fuzz tests for the rules engine and canonical event
parser — Issue #1353.

Five property tests using Hypothesis:
  1. confidence always in [0.0, 1.0]
  2. empty KDE dict → LOW confidence from the rules engine
  3. TLC normalize is idempotent (normalize(normalize(x)) == normalize(x))
  4. event hash is deterministic (same inputs → same hash)
  5. RulesEngine never raises an unhandled exception on arbitrary string input

All DB / external dependencies are mocked so these run fully offline.
"""

