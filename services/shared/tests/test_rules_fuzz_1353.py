# 3. TLC normalize is idempotent
# ---------------------------------------------------------------------------

# Minimal TLC normalizer inline — mirrors the logic in rules engine / extractor
# without importing a potentially DB-bound module.

def _normalize_tlc(value: str) -> str:
    """Canonical TLC normalization: strip whitespace, uppercase."""
    if not isinstance(value, str):
        return ""
    return value.strip().upper()


class TestTlcNormalizeIdempotent:
    """Property: normalizing a TLC twice is the same as normalizing once."""

    @given(raw=_text_st)
    @settings(max_examples=500)
    def test_normalize_is_idempotent(self, raw: str):
        once = _normalize_tlc(raw)
        twice = _normalize_tlc(once)
        assert once == twice, (
            f"TLC normalization not idempotent: {raw!r} → {once!r} → {twice!r}"
        )


# ---------------------------------------------------------------------------
# 4. Event hash is deterministic
# ---------------------------------------------------------------------------

class TestEventHashDeterministic:
    """Property: compute_event_hash returns the same value for identical inputs,
    regardless of call order or KDE dict insertion order."""

    @given(
        event_id=_nonempty_text_st,
        event_type=st.sampled_from(["receiving", "shipping", "transformation", "growing"]),
        tlc=_nonempty_text_st,
        product_description=_text_st,
        quantity=st.floats(min_value=0.0, max_value=1e9, allow_nan=False, allow_infinity=False),
        unit=st.sampled_from(["EA", "LB", "KG", "CS"]),
        timestamp=_nonempty_text_st,
        kdes=_kde_st,
    )
    @settings(max_examples=300)
    def test_hash_is_deterministic(
        self,
        event_id: str,
        event_type: str,
        tlc: str,
        product_description: str,
        quantity: float,
        unit: str,
        timestamp: str,
        kdes: dict,
    ):
        h1 = compute_event_hash(
            event_id=event_id,
            event_type=event_type,
            tlc=tlc,
            product_description=product_description,
            quantity=quantity,
            unit_of_measure=unit,
            location_gln=None,
            location_name=None,
            timestamp=timestamp,
            kdes=kdes,
        )
        h2 = compute_event_hash(
            event_id=event_id,
            event_type=event_type,
            tlc=tlc,
            product_description=product_description,
            quantity=quantity,
            unit_of_measure=unit,
            location_gln=None,
            location_name=None,
            timestamp=timestamp,
            kdes=kdes,
        )
        assert h1 == h2, f"compute_event_hash not deterministic: {h1!r} != {h2!r}"
        assert isinstance(h1, str) and len(h1) == 64, (
            f"Expected 64-char sha256 hex, got {h1!r}"
        )


# ---------------------------------------------------------------------------
# 5. RulesEngine never raises unhandled exception on arbitrary string input
# ---------------------------------------------------------------------------

class TestRulesEngineNoUnhandledException:
    """Property: RulesEngine.evaluate_event must not raise any unhandled
    exception regardless of what arbitrary strings are in the event fields.
    It may return a non-compliant summary, log errors, etc., but must not
    propagate an exception to the caller."""

    @given(
        event_id=_text_st,
        event_type=_text_st,
        tlc=_text_st,
        text_field=_text_st,
    )
    @settings(max_examples=300, deadline=2000)
    def test_no_unhandled_exception_on_arbitrary_input(
        self,
        event_id: str,
        event_type: str,
        tlc: str,
        text_field: str,
    ):
        from services.shared.rules.engine import RulesEngine

        mock_session = MagicMock()
        mock_session.execute.return_value.mappings.return_value.all.return_value = []

        engine = RulesEngine(db_session=mock_session)

        canonical_event = {
            "event_id": event_id,
            "event_type": event_type,
            "tlc": tlc,
            "kdes": {"arbitrary_field": text_field},
            "product_description": text_field,
            "quantity": 0.0,
            "unit_of_measure": "EA",
            "location_gln": None,
            "location_name": None,
            "timestamp": text_field,
        }

        tenant_id = "00000000-0000-0000-0000-000000000001"

        try:
            with patch("services.shared.rules.engine.fetch_related_events", return_value=[]):
                summary = engine.evaluate_event(canonical_event, tenant_id=tenant_id)
            # If we got here, no unhandled exception — that's what we want.
        except SystemExit:
            raise  # let pytest handle sys.exit
        except Exception as exc:
            pytest.fail(
                f"RulesEngine raised unhandled {type(exc).__name__}: {exc}\n"
                f"  event_id={event_id!r}, event_type={event_type!r}, "
                f"tlc={tlc!r}, text_field={text_field!r}"
            )
